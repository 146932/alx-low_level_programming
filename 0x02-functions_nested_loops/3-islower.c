#include "main.h"

/**
  * _islower - Checks for lowercase character
  * @r: The character to be checked
  *
  * Return: 1 for lowercase character or 0 for otherwise
  */
int _islower(int r)
{
	int r;

    r = _islower('H');
    _putchar(r + '0');
    r = _islower('o');
    _putchar(r + '0');
    r = _islower(108);
    _putchar(r + '0');
    _putchar('\n');
    return (0);
}
